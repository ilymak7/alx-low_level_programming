/**
 * op_add - add up two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference between two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: difference between a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: a devided by b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of euclidian division of a by b
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: mod of a b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
